import java.util.Scanner;
public class SwitchCase {
    public static void main(String[] args){
        /*
        * switch(expression){
        *   case 1:
        *       statements;
        *   case 2:
        *       statements;
        *
        *   ...
        *
        *   default:
        *       statement;
        * }
        */

        Scanner scanner = new Scanner(System.in);
        int day = scanner.nextInt();

        switch (day){
            case 1:
                System.out.println("Sunday");
                break;
            case 1:
                System.out.println("Sunday");
                break;
        }


    }
}
