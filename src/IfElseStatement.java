public class IfElseStatement {
    public static void main (String[] args){
        // if(expression){ statement} else{statement}

        int a = 10;
        int b = 20;

        if (a > b) {
            System.out.println("a is greater than b");
        } else {
            System.out.println("a is less than b");
        }

    }
}
